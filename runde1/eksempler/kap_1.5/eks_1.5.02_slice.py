venner = ['Ole','Dole','Doffen','Donald']

# ['Ole', 'Dole', 'Doffen', 'Donald']
print(venner[:])

# ['Dole', 'Doffen']
print(venner[1:3])

# ['Ole', 'Dole']
print(venner[:2])

# ['Doffen', 'Donald']
print(venner[2:])

# ['Dole', 'Donald']
print(venner[1::2])

# ['Donald', 'Doffen', 'Dole', 'Ole']
print(venner[::-1])

# ['Ole', 'Dole', 'Doffen', 'Donald']
print(venner[::3])

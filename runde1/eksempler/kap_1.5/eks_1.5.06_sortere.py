print('sort:')
venner = ['Ole','Dole','Doffen']
print(venner)

# ['Doffen', 'Dole', 'Ole']
venner.sort()
print(venner)

print('sorted:')
venner = ['Ole','Dole','Doffen']
print(sorted(venner))
# ['Doffen', 'Dole', 'Ole']
print(venner)

print('reversed:')
print(venner)
# ['Doffen', 'Dole', 'Ole']
venner.reverse()
print(venner)
